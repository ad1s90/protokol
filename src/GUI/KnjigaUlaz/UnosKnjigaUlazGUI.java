    private void btnSnimiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSnimiActionPerformed
        KnjigaUlazDAO db = new KnjigaUlazDAO();

        try {

            if (!HelpClass.isNum(tfOsnovniBroj.getText())) {
                JOptionPane.showMessageDialog(rootPane, "Osnovni broj mora biti cijeli broj!");
                return;
            }

            if (!HelpClass.dateMatcher(tfDatumPrijema.getText())) {
                JOptionPane.showMessageDialog(rootPane, "Datum mora biti u formatu dd.MM.yyyy!");
                return;
            }

            int osnovniBroj = Integer.valueOf(tfOsnovniBroj.getText());
            String opis = tfOpis.getText();
            Date date = HelpClass.stringToDate(tfDatumPrijema.getText());
            String posiljalac = tfPosiljalac.getText();
            String broj_akta = tfBrojAkta.getText();
            String orgJedinica = comboOrgJedinica.getSelectedItem().toString();

            if (tfPodbroj.getText().equals("")) {
                KnjigaUlaz ki = new KnjigaUlaz(osnovniBroj, opis, date, posiljalac, broj_akta, orgJedinica);
                db.insertKU(ki);
            } else {
                if (!HelpClass.isNum(tfPodbroj.getText())) {
                    JOptionPane.showMessageDialog(rootPane, "Osnovni broj mora biti cijeli broj!");
                    return;
                }
                int podbroj = Integer.valueOf(tfPodbroj.getText());
                KnjigaUlaz ki = new KnjigaUlaz(osnovniBroj, opis, podbroj, date, posiljalac, broj_akta, orgJedinica);
                db.insertKU(ki);

            }

            db.fillTableKU(KnjigaUlazGUI.jTablePrikazKnjigaUlaz);

        } catch (HeadlessException | NumberFormatException | SQLException | ParseException e) {
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(UnosKnjigaUlazGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        HelpClass.clrFields(tfBrojAkta, tfOpis, tfOsnovniBroj, tfPodbroj, tfPosiljalac);

        String datum = HelpClass.localDateFormatter(LocalDate.now());
        tfDatumPrijema.setText(datum);
        try {
            tfOsnovniBroj.setText(String.valueOf(db.maxOsnovniBrojKU() + 1));
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(UnosKnjigaUlazGUI.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnSnimiActionPerformed

