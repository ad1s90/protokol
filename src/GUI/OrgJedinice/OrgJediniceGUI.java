    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        OrgJediniceDAO db = new OrgJediniceDAO();

        String naziv = jtfNaziv.getText();

        if (naziv.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Unesite naziv");
            return;
        }
        OrgJedinice orgJed = new OrgJedinice(naziv);
        try {
            db.insertOrgJed(orgJed);
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(OrgJediniceGUI.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            db.fillTableOrgJed(jTableOrgJedinice);
        } catch (SQLException | IOException | ClassNotFoundException ex) {
            Logger.getLogger(OrgJediniceGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
            int row = jTableOrgJedinice.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(rootPane, "Odaberite red za edit");
                return;
            }
        } catch (HeadlessException e) {
        }
        JDialog dialog = new OrgJediniceEdit(new javax.swing.JFrame(), rootPaneCheckingEnabled);
        dialog.setLocationRelativeTo(this);
        dialog.setVisible(true);

    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            int row = jTableOrgJedinice.getSelectedRow();
            
            if (row == -1) {
                JOptionPane.showMessageDialog(rootPane, "Odaberite red za brisanje");
                return;
            }
            String value = jTableOrgJedinice.getModel().getValueAt(row, 1).toString();
            OrgJediniceDAO db = new OrgJediniceDAO();
            db.deleteOrgJed(Integer.valueOf(value));
            db.fillTableOrgJed(jTableOrgJedinice);

           

        } catch (HeadlessException e) {
        } catch (IOException | SQLException | ClassNotFoundException ex) {
            Logger.getLogger(OrgJediniceGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

